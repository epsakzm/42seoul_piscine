#include "header.h"

int		is_answer(char *s, char *d)
{
	int i;
	int v1111;

	i = 0;
	while (i < 16)
	{
		if (0 <= i && i < 16)
		{
			v1111 = get_v1100(s, i);
			v1111 += get_v0011(s, i);
		}
		if (!is_right_by_condition(v1111, d[i]))
			return (0);
		i++;
	}
	return (1);
}

int		get_v1100(char *s, int i)
{
	int v1100;

	v1100 = 0;
	if (i < 4)
		v1100 = 1000 * (s[i] - '0') + 100 * (s[i + 4] - '0');
	else if (i < 8)
		v1100 = 1000 * (s[i + 8] - '0') + 100 * (s[i + 4] - '0');
	else if (i < 12)
		v1100 = 1000 * (s[4 * i - 32] - '0') + 100 * (s[4 * i - 31] - '0');
	else if (i < 16)
		v1100 = 1000 * (s[4 * i - 45] - '0') + 100 * (s[4 * i - 46] - '0');
	return (v1100);
}

int		get_v0011(char *s, int i)
{
	int v0011;

	v0011 = 0;
	if (i < 4)
		v0011 = 10 * (s[i + 8] - '0') + (s[i + 12] - '0');
	else if (i < 8)
		v0011 += 10 * (s[i] - '0') + (s[i - 4] - '0');
	else if (i < 12)
		v0011 = 10 * (s[4 * i - 30] - '0') + (s[4 * i - 29] - '0');
	else if (i < 16)
		v0011 = 10 * (s[4 * i - 47] - '0') + (s[4 * i - 48] - '0');
	return (v0011);
}
